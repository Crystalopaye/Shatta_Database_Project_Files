CREATE TABLE shatta_music_collection(
    song_id INT,
    title VARCHAR(255),
    audio BOOLEAN,
    video BOOLEAN,
    release_date DATE,
    feature VARCHAR(255),
    featured_by VARCHAR(255),
    producers VARCHAR(255),
    genre VARCHAR(255),
    duration TIME,
    platform VARCHAR(255),
    youtube BOOLEAN,
    release_type VARCHAR(25),
    notes VARCHAR(255)
);